export function generateSalesData(): SalesData[] {
	const data: SalesData[] = []
	const today = new Date()

	for (let i = 29; i >= 0; i--) {
		const date = new Date(today)
		date.setDate(date.getDate() - i)

		const baseRevenue = 4000 + Math.random() * 3000
		const weekendMultiplier = [0, 6].includes(date.getDay()) ? 1.4 : 1
		const revenue = Math.round(baseRevenue * weekendMultiplier)
		const orders = Math.floor(
			50 + Math.random() * 40 + (weekendMultiplier > 1 ? 30 : 0)
		)
		const expenses = Math.round(revenue * (0.4 + Math.random() * 0.1))

		data.push({
			date: date.toISOString().split("T")[0],
			revenue,
			orders,
			expenses,
		})
	}

	return data
}

